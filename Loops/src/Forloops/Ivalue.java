package Forloops;

public class Ivalue {

	public static void main(String[] args) {
		
		int i = 10;
		
		System.out.println("Print i = 10 - " +i);
		
		int j = i++;
		
		System.out.println("Print value of j - " +j);
		
		System.out.println("When j = i++ then i value - " +i); 
		
		int k = ++i;
		
		System.out.println("Print value of k - " +k);
		System.out.println("Print value of i when k = ++i - " +k);
		
		
		
				
				
	}

}
